package modsql

import (
	"bytes"
	"database/sql"
	"fmt"
	"io"
	"log"
	"os"
	"runtime"
	"strings"
	"text/template"
)

const (
	_SQL_FILE   = "model.sql"
	_MODEL_FILE = "model.go" // Go definitions related to each SQL table

	header = "// MACHINE GENERATED BY [ModSQL](github.com/kless/modsql); DO NOT EDIT\n//\n"
)

var _log = log.New(os.Stderr, "ERROR: ", 0)

// Load loads a database from an archive created by modsql.
// If the file name is empty then it is used the name by default.
func Load(db *sql.DB, filename string) error {
	if filename == "" {
		filename = _SQL_FILE
	}

	tmpl, err := template.New("").ParseFiles(filename)
	if err != nil {
		return err
	}

	// Value for the SQL engine's integer according to the architecture.
	intSQLString := struct {
		MySQLInt   string
		PostgreInt string
	}{
		// architecture of 64-bits
		"BIGINT",
		"bigint",
	}

	if runtime.GOARCH != "amd64" {
		intSQLString.MySQLInt = "INT"
		intSQLString.PostgreInt = "integer"
	}

	buf := new(bytes.Buffer)
	if err = tmpl.Execute(buf, intSQLString); err != nil {
		return err
	}

	firstLine := ""

	for {
		line, err := buf.ReadString('\n')
		if err == io.EOF {
			break
		}

		line = strings.TrimSpace(line)
		if line == "" || strings.HasPrefix(line, "//") {
			continue
		}

		firstLine += line

		if !strings.HasSuffix(line, ";") {
			continue
		}
		if _, err = db.Exec(firstLine); err != nil {
			return fmt.Errorf("SQL line: %s\n%s", firstLine, err)
		}
		firstLine = ""
	}

	return nil
}
